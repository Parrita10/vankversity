/*Escribe un bucle while que imprima los números del -100 al 600*/

let num = -100; // Inicializamos la variable en -100

while (num <= 600) { // Condición: el bucle se ejecuta mientras num sea menor o igual a 600
  console.log(num); // Imprimimos el valor actual de num
  num++; // Incrementamos num en 1
}

